   public static void validate() {
      Map<Pair<CreativeModeTab.Row, Integer>, String> positions = new HashMap();

      for(ResourceKey tabKey : BuiltInRegistries.CREATIVE_MODE_TAB.registryKeySet()) {
         CreativeModeTab tab = (CreativeModeTab)BuiltInRegistries.CREATIVE_MODE_TAB.getValueOrThrow(tabKey);
         String current = tab.getDisplayName().getString();
         String previous = (String)positions.put(Pair.of(tab.row(), tab.column()), current);
         if (previous != null) {
            throw new IllegalArgumentException("Duplicate position: " + current + " vs. " + previous);
         }
      }

   }

   public static CreativeModeTab getDefaultTab() {
      return (CreativeModeTab)BuiltInRegistries.CREATIVE_MODE_TAB.getValueOrThrow(BUILDING_BLOCKS);
   }

   private static void generatePotionEffectTypes(final CreativeModeTab.Output output, final HolderLookup potions, final Item item, final CreativeModeTab.TabVisibility tabVisibility, final FeatureFlagSet enabledFeatures) {
      potions.listElements().filter((potion) -> ((Potion)potion.value()).isEnabled(enabledFeatures)).map((potion) -> PotionContents.createItemStack(item, potion)).forEach((stack) -> output.accept(stack, tabVisibility));
   }

